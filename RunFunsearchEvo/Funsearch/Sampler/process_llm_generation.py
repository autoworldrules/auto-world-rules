"""
Process LLM-generated code to extract valid Python functions.

This module provides utilities for cleaning and validating LLM-generated code,
following DeepMind's FunSearch approach. LLMs often generate code wrapped in
markdown, with conversational text, or with syntax errors. These utilities
extract and validate the actual Python function from such output.
"""

import ast
import re

# ---------------------------------------------------------------------------
# GraphWranglingMethods toolkit registry
# Mirrors Funsearch/GraphWranglingMethods/__all__ — update both if functions change.
# ---------------------------------------------------------------------------
_TOOLKIT_FUNCTIONS: frozenset[str] = frozenset([
    # parsing
    "parse_atom", "break_edge", "text2edges", "build_graph", "build_simple_graph",
    # path_analysis
    "has_path", "shortest_path_length", "count_paths", "count_trails",
    "all_shortest_paths", "paths_with_relations", "graph_diameter", "eccentricity",
    # structure
    "node_degree", "get_neighbors", "get_edge_types_between", "get_all_edge_types",
    "count_edges_of_type", "num_nodes", "num_edges",
    "connected_components", "strongly_connected_components", "has_cycle",
    "betweenness_centrality", "pagerank", "in_degree_centrality", "out_degree_centrality",
])


def strip_markdown_and_text(generated_text: str) -> str:
    """
    Remove markdown code blocks and conversational text, extract just Python code.
    
    LLMs often wrap code in markdown blocks (```python ... ```) and add
    explanatory text before/after the code. This function extracts only the
    Python code portion.
    
    Args:
        generated_text (str): Raw text generated by the LLM, which may contain:
            - Markdown code fences: ```python ... ```
            - Conversational text: "Here is the function:", "Please note..."
            - Multiple code blocks
            - Mixed text and code
            
    Returns:
        str: Extracted Python code without markdown or conversational text.
             If no code is found, returns the original text unchanged.
             
    Examples:
        >>> text = '''Here is a function:
        ... ```python
        ... def priority(x):
        ...     return x * 2
        ... ```
        ... Please note this is just an example.'''
        >>> result = strip_markdown_and_text(text)
        >>> print(result)
        def priority(x):
            return x * 2
            
    Behavior:
        - Detects markdown code blocks starting with ``` and ending with ```
        - Extracts the first code block containing 'def priority'
        - If no markdown is present, returns text starting from first 'def priority'
        - Preserves all lines within the detected code block
    """
    lines = generated_text.split('\n')
    code_lines = []
    in_code_block = False
    found_function = False
    
    for i, line in enumerate(lines):
        # Check for markdown code block markers
        if line.strip().startswith('```'):
            if not in_code_block:
                # Starting a code block
                in_code_block = True
            else:
                # Ending a code block
                in_code_block = False
                if found_function:
                    # We found our function, stop here
                    break
            continue
        
        # If we're in a code block or not using markdown, collect code
        if in_code_block or not any('```' in l for l in lines):
            # Look for function definition
            if line.strip().startswith('def priority'):
                found_function = True
                in_code_block = True  # Force collection even without markdown
            
            if in_code_block and found_function:
                code_lines.append(line)
    
    # If we found nothing, return original text
    if not code_lines:
        return generated_text
    
    return '\n'.join(code_lines)


def validate_function_has_return(function_code: str) -> bool:
    """
    Validate that a function has valid Python syntax and at least one return statement.
    
    Uses Python's Abstract Syntax Tree (AST) parser to verify the code structure.
    This follows DeepMind's FunSearch approach of validating generated code before
    execution.
    
    Args:
        function_code (str): Complete Python function definition as a string.
            Must be valid Python code with 'def function_name(...): ...'
            
    Returns:
        bool: True if the function:
            - Has valid Python syntax (parses successfully)
            - Contains exactly one function definition
            - Has at least one return statement in its body
            
            False if:
            - Syntax is invalid
            - Multiple or zero function definitions found
            - No return statement present
            - Any parsing error occurs
            
    Examples:
        >>> code = '''def priority(x):
        ...     if x > 0:
        ...         return 1.0
        ...     return 0.0'''
        >>> validate_function_has_return(code)
        True
        
        >>> incomplete = '''def priority(x):
        ...     x = x + 1'''  # Missing return
        >>> validate_function_has_return(incomplete)
        False
        
    Notes:
        - Silent failure: Returns False for any exception without raising
        - Prints diagnostic message if return statement is missing
        - Used to filter out incomplete LLM generations before execution
    """
    try:
        tree = ast.parse(function_code)
        function_defs = [node for node in ast.walk(tree) if isinstance(node, ast.FunctionDef)]
        
        if len(function_defs) != 1:
            return False
        
        func_def = function_defs[0]
        has_return = any(isinstance(node, ast.Return) for node in ast.walk(func_def))
        
        if not has_return:
            print(f"Function validation failed: no return statement found")
            return False
            
        return True
    except Exception as e:
        # Silent failure - any parsing error means invalid function
        return False


def extract_function_from_llm_output(generated_text: str) -> tuple[str, bool]:
    """
    Extract a complete, valid Python function from LLM-generated text.
    
    This is the main entry point for processing LLM output. It handles:
    1. Markdown code blocks (```python ... ```)
    2. Conversational text before/after code
    3. Multiple function definitions (takes only the first priority_v function)
    4. Validation of completeness and correct parameter order
    
    Expected parameter order for priority function:
    priority(cand_fact: str, definite_rules_program: str, entailed_facts: str, facts_program: str)
    
    Args:
        generated_text (str): Raw text output from an LLM, which may contain:
            - Markdown code blocks (```python ... ```)
            - Conversational text ("Here's a function:", etc.)
            - Multiple functions (we take first priority_v* function)
            - Comments and explanatory text
            
    Returns:
        tuple[str, bool]: A tuple containing:
            - str: The extracted function code as a string. Empty string if extraction failed.
            - bool: True if extraction succeeded and function is valid (has return statement),
                   False if extraction failed or function is incomplete.
    """
    # Step 1: Strip markdown code blocks and conversational text
    code_only = strip_markdown_and_text(generated_text)
    
    if not code_only.strip():
        print("Function extraction failed: No code found after stripping markdown")
        return '', False
    
    # Step 2: Extract just the first priority_v* function
    # LLM might generate multiple functions or extra code
    lines = code_only.split('\n')
    function_lines = []
    inside_function = False
    function_found = False
    base_indent = None
    in_docstring = False
    docstring_delimiter = None
    
    for line in lines:
        stripped = line.strip()
        
        # Look for function definition starting with 'def priority'
        if stripped.startswith('def priority'):
            if function_found:
                # We already found a function, stop here (prevent multiple functions)
                break
            inside_function = True
            function_found = True
            function_lines.append(line)
            base_indent = len(line) - len(line.lstrip())
            
            # Validate parameter order
            # Expected: cand_fact, definite_rules_program, entailed_facts, facts_program
            param_match = re.search(r'def\s+priority[_v\d]*\s*\((.*?)\)\s*->', line)
            if param_match:
                params_str = param_match.group(1)
                # Extract just parameter names (ignore type hints and spaces)
                param_names = [p.split(':')[0].strip() for p in params_str.split(',') if p.strip()]
                
                expected_order = ['cand_fact', 'definite_rules_program', 'entailed_facts', 'facts_program']
                if param_names != expected_order:
                    print(f"Warning: Parameter order mismatch. Expected {expected_order}, got {param_names}")
                    print(f"Will attempt to reorder parameters to match expected signature")
                    # Note: We'll continue and rely on the renaming step to fix this
            
            continue
        
        if not inside_function:
            continue
        
        # Track docstrings
        if not in_docstring and (stripped.startswith('"""') or stripped.startswith("'''")):
            delimiter = '"""' if stripped.startswith('"""') else "'''"
            docstring_delimiter = delimiter
            in_docstring = True
            function_lines.append(line)
            if stripped.count(delimiter) >= 2:
                in_docstring = False
                docstring_delimiter = None
            continue
        
        if in_docstring:
            function_lines.append(line)
            if docstring_delimiter in line:
                in_docstring = False
                docstring_delimiter = None
            continue
        
        # Empty lines are OK
        if not stripped:
            function_lines.append(line)
            continue
        
        # Check indentation to detect end of function
        current_indent = len(line) - len(line.lstrip())
        
        if current_indent <= base_indent and stripped:
            # End of function - check if it's explanatory text
            if stripped.startswith('#') or stripped.startswith('def ') or stripped.startswith('class '):
                break
            # Also stop at common explanatory patterns
            if any(stripped.startswith(p) for p in ['In this', 'This function', 'Note that', 'The above']):
                break
        
        # Part of function body
        function_lines.append(line)
    
    if not function_lines:
        print("Function extraction failed: No function definition found")
        return '', False
    
    result = '\n'.join(function_lines)
    
    # Rename priority_vX to priority (standardize function name)
    result = re.sub(r'\bpriority_v\d+\b', 'priority', result)
    
    # Ensure the signature matches expected order
    # Replace any incorrect parameter order with the correct one
    def_line_match = re.search(r'(def\s+priority\s*\()([^)]+)(\)\s*->.*?:)', result)
    if def_line_match:
        prefix = def_line_match.group(1)
        params = def_line_match.group(2)
        suffix = def_line_match.group(3)
        
        # Always enforce the correct signature
        correct_params = "cand_fact: str, definite_rules_program: str, entailed_facts: str, facts_program: str"
        result = result.replace(def_line_match.group(0), f"{prefix}{correct_params}{suffix}")
    
    # Step 3: Validate it has a return statement
    is_valid = validate_function_has_return(result)
    
    if not is_valid:
        print(f"Function extraction failed validation")
        print(f"Extracted code was:\n{result[:500]}...")
    
    # Step 4: Auto-inject imports for any GraphWranglingMethods functions used.
    # This removes any import the LLM may have written itself, then injects a
    # single canonical import for exactly the names that are actually called.
    if is_valid:
        used_names = detect_toolkit_usage(result)
        result = inject_toolkit_imports(result, used_names)
    
    return result, is_valid


