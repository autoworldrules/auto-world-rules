def detect_toolkit_usage(function_code: str) -> list[str]:
    """Return the sorted list of GraphWranglingMethods function names called in *function_code*.

    Method 1 (primary): AST walk — finds ``ast.Call`` nodes whose callee is a bare
    ``ast.Name`` matching a known toolkit name.  Ignores occurrences inside string
    literals or import statements.

    Method 2 (fallback): Regex scan — used when the code has a syntax error that
    prevents AST parsing.  Looks for ``toolkit_name(`` patterns.

    Args:
        function_code: Extracted Python function string.

    Returns:
        Sorted list of toolkit function names that are called, e.g.
        ``['build_simple_graph', 'has_path', 'text2edges']``.
    """
    used: set[str] = set()

    try:
        # Method 1: AST-based (precise)
        tree = ast.parse(function_code)
        for node in ast.walk(tree):
            if (
                isinstance(node, ast.Call)
                and isinstance(node.func, ast.Name)
                and node.func.id in _TOOLKIT_FUNCTIONS
            ):
                used.add(node.func.id)
    except SyntaxError:
        # Method 2: Regex fallback
        pattern = (
            r'\b('
            + '|'.join(re.escape(name) for name in sorted(_TOOLKIT_FUNCTIONS))
            + r')\s*\('
        )
        for m in re.finditer(pattern, function_code):
            used.add(m.group(1))

    return sorted(used)


def inject_toolkit_imports(function_code: str, used_names: list[str]) -> str:
    """Inject a ``from Funsearch.GraphWranglingMethods import ...`` line into the function.

    Any existing ``GraphWranglingMethods`` import lines anywhere in the code are
    removed first (normalising any import the LLM may have written itself).  Then
    a single import for exactly *used_names* is inserted as the first statement
    of the function body, after the ``def`` line and any opening docstring.

    Returns *function_code* unchanged when *used_names* is empty.

    Args:
        function_code: Extracted Python function string.
        used_names: Sorted list of toolkit names to import (from :func:`detect_toolkit_usage`).

    Returns:
        Function string with the import injected (or the original if nothing to inject).
    """
    if not used_names:
        return function_code

    lines = function_code.split('\n')

    # Remove any pre-existing GraphWranglingMethods import lines
    lines = [l for l in lines if 'GraphWranglingMethods' not in l]

    insert_idx = _find_body_start(lines)

    # Determine indentation from the first body line; fall back to 4 spaces
    if insert_idx < len(lines) and lines[insert_idx].strip():
        indent = ' ' * (len(lines[insert_idx]) - len(lines[insert_idx].lstrip()))
    else:
        indent = '    '

    import_line = (
        f"{indent}from Funsearch.GraphWranglingMethods import "
        + ', '.join(used_names)
    )
    lines.insert(insert_idx, import_line)
    return '\n'.join(lines)


def _find_body_start(lines: list[str]) -> int:
    """Return the index of the first non-docstring body line inside a function.

    Scans *lines* (which must start with the ``def`` line at index 0) and
    skips the ``def`` line itself plus any opening docstring, then returns the
    index of the first real statement.

    Returns ``len(lines)`` when no body is found (e.g. stub function).
    """
    in_docstring = False
    docstring_delim: str | None = None

    for i, line in enumerate(lines):
        if i == 0:
            continue  # always skip the def line

        stripped = line.strip()

        if in_docstring:
            if docstring_delim and docstring_delim in stripped:
                in_docstring = False
            continue

        if not stripped:
            continue  # skip blank lines between def and docstring / body

        # Check whether this line *opens* a docstring
        for delim in ('"""', "'''"):
            if stripped.startswith(delim):
                rest = stripped[len(delim):]  # everything after the opening delimiter
                if delim in rest:
                    # Delimiter closes on the same line → single-line docstring
                    break  # skip this line and keep looking for body start
                else:
                    # Multi-line docstring starts here
                    in_docstring = True
                    docstring_delim = delim
                    break
        else:
            # Not a docstring opener → this is the first body statement
            return i

    return len(lines)
