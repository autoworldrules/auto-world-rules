def _generate_story_and_queries_worker(
    priority_fn_str: str,
    story_idx: int,
    seed: int,
    rules_path: str,
    min_entities: int,
    max_entities: int,
    num_cands: int,
) -> pd.DataFrame:
    """Top-level worker function for parallel story generation (must be picklable).

    Converts *priority_fn_str* to a callable, generates one story, builds
    the corresponding query DataFrame, and tags it with *story_idx*.
    """
    namespace: dict = {}
    exec(priority_fn_str, namespace)
    priority_fn = namespace['priority']

    generator_story = PrioStoryGeneratorNoRa1_1(
        priority_fn=priority_fn,
        seed=seed,
        min_entities=min_entities,
        max_entities=max_entities,
        num_cands=num_cands,
    )
    story_info = generator_story.generate_story_from_rules(rules_path)

    generator_queries = StoryQueryGeneratorNoRa1_1()
    df_queries = generator_queries.build_query_dataframe(story_info)
    df_queries['story_id'] = story_idx
    return df_queries


def compute_min_correct_logprobs(logits: torch.Tensor, labels_mask: torch.Tensor) -> float:
    """
    Compute min of minimum logits for correct labels per query.

    For each query, finds the minimum logit among the correct label positions,
    then returns the min across all queries.

    Args:
        logits: Model output logits, shape (Q, num_labels)
        labels_mask: Multi-hot boolean mask of correct labels, shape (Q, num_labels)

    Returns:
        Min of minimum correct logits across queries
    """
    # Create query indices (1-indexed to distinguish from 0 which marks incorrect labels)
    # Shape: (Q, 1)
    Q = labels_mask.shape[0]
    indices = torch.arange(1, Q + 1, device=logits.device).unsqueeze(-1)

    # Multiply indices by mask: correct positions get their query index, others get 0
    # Shape: (Q, num_labels) -> ravel to (Q * num_labels,)
    index = (indices * labels_mask).ravel()

    # Flatten logits to match
    logits_flat = logits.ravel()

    # scatter_min groups by index and takes minimum
    # Index 0 collects all incorrect positions (we ignore it)
    # Indices 1..Q collect correct positions for each query
    min_correct_logits, _ = scatter_min(logits_flat, index, dim=-1)

    # min_correct_logits[0] is garbage (incorrect positions), skip it
    # Take min of min_correct_logits[1:Q+1]
    return min_correct_logits[1:Q+1].min().item()


